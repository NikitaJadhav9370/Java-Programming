package Operators;

public class AssignmentOperator {

	public static void main(String[] args) {
	
	//............+=..........//
		int A=10;
		// A=A+10;
		A+=12;
		System.out.println("Output For += : " + A);
		
		int B=37;
		// B=B-2;
		B-=2;
		System.out.println("Output For -= : " + B);
		
		int C=4;
		// C =C*5;
		C*=5;
		System.out.println("Output For *= : " + C);
		
		
		int D=25;
		// D =D/5;
		D/=5;
		System.out.println("Output For /= : " + D);
		
		int E=26;
		// E =E%5;
		E%=5;
		System.out.println("Output For %= : " + E);
		
 
		
		
		
		

	}

}
