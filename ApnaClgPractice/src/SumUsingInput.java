import java.util.*;
public class SumUsingInput {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int a=sc.nextInt();
		int b=sc.nextInt();
		int sum=a+b;
		System.out.println(sum);
		
		
		
// Product of two numbers a and b......
		int num1=sc.nextInt();
		int num2=sc.nextInt();
		int Multiple=num1*num2;
		System.out.println(Multiple);
	}
}
